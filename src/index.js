const authenticateAdmin = (req, res, next) => {
  const apiKey = req.headers['x-api-key'];
  if (apiKey === process.env.ADMIN_API_KEY) return next();
  return res.status(401).json({ error: 'Unauthorized: Valid Admin API Key required' });
};


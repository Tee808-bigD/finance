function createAdaptiveCard(title, facts, status = 'Attention') {
  return {
    type: 'message',
    attachments: [{
      contentType: 'application/vnd.microsoft.card.adaptive',
      content: {
        type: 'AdaptiveCard',
        version: '1.4',
        body: [
          { type: 'TextBlock', text: title, weight: 'Bolder', size: 'Medium', color: status === 'Success' ? 'Good' : 'Attention' },
          { type: 'FactSet', facts: facts.map(f => ({ title: f.label, value: f.value })) },
          { type: 'TextBlock', text: 'Generated by Finance Intelligence Agent', size: 'Small', isSubtle: true, spacing: 'Medium' }
        ],
        actions: [{ type: 'Action.OpenUrl', title: 'View Company Policy', url: 'https://company.sharepoint.com/policies' }]
      }
    }]
  };
}

